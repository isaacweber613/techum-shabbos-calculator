import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const OUTPUT_DIR = path.join(ROOT, "docs", "agent-index");
const CHECK = process.argv.includes("--check");
const NOTICE = "<!-- Generated by `npm run agent:index`. Do not edit by hand. -->";
const files = execFileSync("git", ["ls-files", "-z"], {
  cwd: ROOT,
  encoding: "utf8",
})
  .split("\0")
  .filter(Boolean)
  .map((file) => file.replaceAll("\\", "/"));

const read = (file) => readFileSync(path.join(ROOT, file), "utf8");
const compact = (value, limit = 180) => {
  const normalized = value.replace(/\s+/g, " ").trim();
  return normalized.length <= limit
    ? normalized
    : `${normalized.slice(0, limit - 1)}…`;
};

function exportsUnder(prefixes) {
  const entries = new Map();
  const add = (name, signature, file) => {
    entries.set(`${file}|${name}`, { name, signature: compact(signature), file });
  };
  for (const file of files.filter(
    (candidate) =>
      prefixes.some((prefix) => candidate.startsWith(prefix)) &&
      /\.(?:m?js|tsx?)$/.test(candidate) &&
      !candidate.includes(".test.") &&
      !candidate.endsWith(".d.ts"),
  )) {
    const source = read(file);
    for (const match of source.matchAll(
      /export\s+(?:default\s+)?(?:async\s+)?function\s+([A-Za-z_$][\w$]*)\s*(\([^)]*\)(?:\s*:\s*[^{\n]+)?)/g,
    )) {
      add(match[1], match[2], file);
    }
    for (const match of source.matchAll(
      /export\s+(?:default\s+)?(class|interface|type|enum)\s+([A-Za-z_$][\w$]*)/g,
    )) {
      add(match[2], match[1], file);
    }
    for (const match of source.matchAll(
      /export\s+(?:const|let|var)\s+([A-Za-z_$][\w$]*)(?:\s*:\s*([^=;\n]+))?/g,
    )) {
      add(match[1], match[2] ?? "inferred", file);
    }
    for (const match of source.matchAll(/export\s*\{([^}]+)\}/g)) {
      for (const item of match[1].split(",")) {
        const parts = item.trim().split(/\s+as\s+/);
        if (parts[0]) add(parts[1] ?? parts[0], "re-export", file);
      }
    }
  }
  return [...entries.values()].sort(
    (a, b) => a.file.localeCompare(b.file) || a.name.localeCompare(b.name),
  );
}

function renderPages() {
  const html = files
    .filter((file) => file.endsWith(".html") && !file.startsWith("dist/"))
    .map((file) => ({
      route:
        file === "index.html"
          ? "/"
          : file.endsWith("/index.html")
            ? `/${file.slice(0, -"/index.html".length)}/`
            : `/${file}`,
      title: read(file).match(/<title>([\s\S]*?)<\/title>/i)?.[1]?.trim() ?? "—",
      file,
    }));
  const pages = exportsUnder(["src/pages/"]).filter(({ name }) => /^[A-Z]/.test(name));
  return `${NOTICE}
# Pages

## Static HTML (${html.length})
| URL | Title | Source |
| --- | --- | --- |
${html.map(({ route, title, file }) => `| \`${route}\` | ${compact(title).replaceAll("|", "\\|")} | \`${file}\` |`).join("\n")}

## Page modules (${pages.length})
| Page | Source |
| --- | --- |
${pages.map(({ name, file }) => `| \`${name}\` | \`${file}\` |`).join("\n")}
`;
}

function discoverRoutes() {
  const routes = new Map();
  const add = (routePath, method, file, handler = "—") => {
    routes.set(`${routePath}|${method}|${file}|${handler}`, {
      path: routePath,
      method,
      file,
      handler,
    });
  };
  for (const file of files.filter(
    (candidate) =>
      /\.(?:m?js|tsx?)$/.test(candidate) &&
      !candidate.includes(".test.") &&
      !candidate.endsWith(".d.ts"),
  )) {
    const contents = read(file);
    for (const match of contents.matchAll(
      /<Route\b[\s\S]{0,300}?\bpath\s*=\s*["']([^"']+)["'][\s\S]{0,300}?\belement\s*=\s*\{\s*<([A-Z][A-Za-z0-9_]*)/g,
    )) {
      add(match[1], "UI", file, match[2]);
    }
    for (const match of contents.matchAll(
      /(?:url\.)?pathname\s*===\s*["']([^"']+)["']\s*&&\s*(?:request|req)\.method\s*===\s*["'](GET|POST|PUT|PATCH|DELETE|HEAD|OPTIONS)["']/g,
    )) {
      add(match[1], match[2], file);
    }
    for (const match of contents.matchAll(
      /(?:request|req)\.method\s*===\s*["'](GET|POST|PUT|PATCH|DELETE|HEAD|OPTIONS)["']\s*&&\s*(?:url\.)?pathname\s*===\s*["']([^"']+)["']/g,
    )) {
      add(match[2], match[1], file);
    }
    for (const match of contents.matchAll(/new RegExp\(\s*`(\^\/[^`]+)`/g)) {
      add(`regex:${match[1]}`, "dynamic", file);
    }
    for (const match of contents.matchAll(
      /pathname\.match\(\s*(\/\^\\\/[^/]+\/[gimsuy]*)\s*\)/g,
    )) {
      add(`regex:${match[1]}`, "dynamic", file);
    }
  }
  return [...routes.values()].sort(
    (a, b) =>
      a.path.localeCompare(b.path) ||
      a.method.localeCompare(b.method) ||
      a.file.localeCompare(b.file),
  );
}

function renderRoutes() {
  const routes = discoverRoutes();
  return `${NOTICE}
# Routes

Static discovery of browser and HTTP entry points. Dynamic regex routes are shown as patterns.

| Path/pattern | Method | Handler/page | Source |
| --- | --- | --- | --- |
${routes.map(({ path: routePath, method, handler, file }) => `| \`${routePath.replaceAll("|", "\\|")}\` | ${method} | \`${handler}\` | \`${file}\` |`).join("\n")}
`;
}

function schemaObjects() {
  const objects = [];
  const definitions = [
    ["table", /create\s+table\s+(?:if\s+not\s+exists\s+)?(?:public\.)?["']?([a-zA-Z0-9_]+)["']?/gi],
    ["view", /create\s+(?:or\s+replace\s+)?view\s+(?:public\.)?["']?([a-zA-Z0-9_]+)["']?/gi],
    ["function", /create\s+(?:or\s+replace\s+)?function\s+(?:public\.)?["']?([a-zA-Z0-9_]+)["']?/gi],
    ["policy", /create\s+policy\s+["']?([^"'\n]+?)["']?\s+on\s+(?:public\.)?["']?([a-zA-Z0-9_]+)["']?/gi],
    ["trigger", /create\s+trigger\s+["']?([a-zA-Z0-9_]+)["']?/gi],
  ];
  for (const file of files.filter((candidate) => candidate.endsWith(".sql"))) {
    const sql = read(file);
    for (const [kind, pattern] of definitions) {
      for (const match of sql.matchAll(pattern)) {
        objects.push({
          kind,
          name: kind === "policy" ? `${match[2]}: ${match[1].trim()}` : match[1],
          file,
        });
      }
    }
  }
  return objects.sort(
    (a, b) => a.kind.localeCompare(b.kind) || a.name.localeCompare(b.name),
  );
}

function renderSchema() {
  const objects = schemaObjects();
  return `${NOTICE}
# Database schema history

Objects declared by tracked SQL migrations. This is a migration map, not a substitute for live-schema/RLS inspection.

| Kind | Object | Migration |
| --- | --- | --- |
${objects.map(({ kind, name, file }) => `| ${kind} | \`${name.replaceAll("|", "\\|")}\` | \`${file}\` |`).join("\n")}
`;
}

function renderComponents() {
  const components = exportsUnder(["src/components/"]).filter(({ name }) =>
    /^[A-Z]/.test(name),
  );
  return `${NOTICE}
# Components

Exported React components under \`src/components/\`.

| Component | Props/signature | Source |
| --- | --- | --- |
${components.map(({ name, signature, file }) => `| \`${name}\` | \`${signature.replaceAll("|", "\\|")}\` | \`${file}\` |`).join("\n")}
`;
}

function renderModules() {
  const exports = exportsUnder([
    "js/",
    "worker/",
    "src/lib/",
    "src/hooks/",
    "src/context/",
    "cloudflare/",
  ]);
  return `${NOTICE}
# Modules

Top-level exports from application, library, hook, context, and worker modules.

| Export | Signature/type | Source |
| --- | --- | --- |
${exports.map(({ name, signature, file }) => `| \`${name}\` | \`${signature.replaceAll("|", "\\|")}\` | \`${file}\` |`).join("\n")}
`;
}

const outputs = new Map([
  ["routes.md", renderRoutes()],
  ["pages.md", renderPages()],
  ["schema.md", renderSchema()],
  ["components.md", renderComponents()],
  ["modules.md", renderModules()],
]);

mkdirSync(OUTPUT_DIR, { recursive: true });
let stale = false;
for (const [name, contents] of outputs) {
  const output = path.join(OUTPUT_DIR, name);
  const normalizedContents = `${contents.trimEnd()}\n`;
  if (CHECK) {
    let existing = "";
    try {
      existing = readFileSync(output, "utf8");
    } catch {
      // Missing output is stale.
    }
    if (existing !== normalizedContents) {
      console.error(`Agent index is stale: docs/agent-index/${name}`);
      stale = true;
    }
  } else {
    writeFileSync(output, normalizedContents, "utf8");
  }
}
if (stale) process.exit(1);
console.log(
  CHECK
    ? "Agent index is current."
    : `Generated ${outputs.size} agent index files in docs/agent-index/.`,
);
